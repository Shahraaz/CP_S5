void solveCase()
{
    ll x, y, a, b;
    cin >> x >> y >> a >> b;
    ll one = 0;
    //bring x to y
    one = abs(y - x) * a + y * b;
    //bring y to x
    one = min(one, abs(y - x) * a + x * b);
    //bring seperately
    one = min(one, x * a + y * a);
    cout << one << '\n';
}

