struct Solution
{
    Solution() {}
    void solveCase()
    {
        ll a, b;
        cin >> a >> b;
        vector<int> cnt = {6, 2, 5, 5, 4, 5, 6, 3, 7, 6};
        ll res = 0;
        while (a <= b)
        {
            auto A = to_string(a);
            for (auto &x : A)
                res += cnt[x - '0'];
            a++;
        }
        cout << res << '\n';
    }
};

