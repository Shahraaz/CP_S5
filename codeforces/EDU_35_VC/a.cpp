#define all(v) v.begin(), v.end()

const int NAX = 2e5 + 5, MOD = 1000000007;

void solveCase()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &x : a)
        cin >> x;
    int mini = *min_element(all(a));
    int prev = -MOD;
    int res = MOD;
    for (int i = 0; i < n; i++)
    {
        if (a[i] == mini)
        {
            res = min(res, i - prev);
            prev = i;
        }
    }
    cout << res << '\n';
}

