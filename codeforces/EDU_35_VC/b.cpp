const int NAX = 2e5 + 5, MOD = 1000000007;

void solveCase()
{
    int n, a, b;
    cin >> n >> a >> b;
    int res = 0;
    for (int typeOne = 1; typeOne < n; typeOne++)
    {
        int two = n - typeOne;
        int temp = MOD;
        temp = min(temp, a / typeOne);
        temp = min(temp, b / two);
        db(typeOne, two, temp);
        res = max(res, temp);
    }
    cout << res << '\n';
}

