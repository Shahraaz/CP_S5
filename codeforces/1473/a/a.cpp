void solveCase()
{
    int n, d;
    cin >> n >> d;
    vector<int> a(n);
    for (auto &x : a)
        cin >> x;
    sort(all(a));
    if (a.back() <= d || (a[0] + a[1]) <= d)
        cout << "YES\n";
    else
        cout << "NO\n";
}

