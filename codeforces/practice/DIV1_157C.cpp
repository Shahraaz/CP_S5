const int NAX = 1e5 + 5, MOD = 1000000007;

int dp[NAX];

const int kmod = 1000000007;

int mul(int a, int b, int mod = kmod)
{
    return (long long)a * b % mod;
}

int add(int a, int b, int mod = kmod)
{
    a += b;
    if (a >= mod)
        a -= mod;
    return a;
}

int sub(int a, int b, int mod = kmod)
{
    a -= b;
    if (a < 0)
        a += mod;
    return a;
}

int power(int base, int index, int mod = kmod)
{
    if (index == 0)
        return 1;
    int temp = power(base, index / 2, mod);
    temp = mul(temp, temp, mod);
    if (index & 1)
        temp = mul(temp, base, mod);
    return temp;
}

const int NAX_PRIME = 1e5 + 5;
vector<int> factors[NAX_PRIME];

void seive(int n = NAX_PRIME)
{
    for (int i = 1; i < n; i++)
        for (int j = i; j < n; j += i)
            factors[j].pb(i);
}

class Solution
{
private:
public:
    Solution()
    {
        seive();
    }
    ~Solution() {}
    void solveCase()
    {
        int n;
        cin >> n;
        vector<int> a(n);
        for (auto &x : a)
            cin >> x;
        sort(all(a));
        const int lim = *max_element(all(a));
        int res = 0;
        auto getGreater = [&a](int start) -> int {
            return a.end() - lower_bound(all(a), start);
        };
        for (int i = 1; i <= lim; i++)
        {
            int j = factors[i].size() - 1;
            int prev = getGreater(factors[i][j]);
            db(prev, j + 1, j);
            int temp = sub(power(j + 1, prev), power(j, prev));
            j--;
            while (j >= 0)
            {
                auto t = getGreater(factors[i][j]);
                db(prev, j + 1);
                temp = mul(temp, power(j + 1, t - prev));
                prev = t;
                j--;
            }
            pc(factors[i]);
            res = add(res, temp);
            db(i, temp, res, '\n');
        }
        cout << res << '\n';
    }
};

