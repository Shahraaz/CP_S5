void solveCase()
{
    ll a, m;
    // cin >> a >> m;
    m = Random<int>(2, (int)1e3)(rng);
    a = Random<int>(1, m - 1)(rng);
    ll temp = __gcd(a, m);
    ll less = (m) / temp;
    ll more = (a + m - 1) / temp;
    vector<pair<int, int>> res;
    int cnta, cntb, cntc;
    cnta = cntb = cntc = 0;
    for (int i = a; i < a + m; i++)
    {
        // if (i % temp == 0)
        {
            ll temp2 = __gcd((ll)i, m);
            // db(i, temp2, temp);
            if (temp2 == temp)
            {
                res.pb({i, __gcd((ll)i, a)});
                if (res.back().s == 1)
                    cnta++;
                else if (res.back().s == a)
                    cntb++;
                else
                    cntc++;
            }
        }
    }
    db(a, m, res.size(), less, temp);
    db(cnta, cntb, cntc);
    // pc(res);
    cout << more - less << '\n';
}

