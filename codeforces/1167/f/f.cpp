struct Bit
{
    int size;
    vector<long long> table;
    Bit(int size)
    {
        this->size = size;
        table.resize(size);
    }
    void update(int i, int delta)
    {
        while (i < size)
        {
            table[i] += delta;
            i = i | (1 + i);
        }
    }
    long long sum(int i)
    {
        long long ret = 0;
        while (i >= 0)
        {
            ret += table[i];
            i = (i & (i + 1)) - 1;
        }
        return ret;
    }
    long long rangeSum(int i, int j)
    {
        if (i == 0)
            return sum(j);
        return sum(j) - sum(i - 1);
    }
    void print()
    {
#ifdef LOCAL
        for (int i = 0; i < size; i++)
            cout << rangeSum(i, i) << ' ';
        cout << '\n';
#else

#endif
    }
};

int s[2][NAX];

void solveCase()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &x : a)
        cin >> x;
    int res = 0;
    for (size_t k = 0; k < 2; k++)
    {
        Bit bb(n + 1);
        auto dx = a;
        sort(all(dx));

        for (size_t i = 0; i < n; i++)
        {
            int pos = lower_bound(all(dx), a[i]) - dx.begin();
            s[k][i] = bb.sum(pos) % MOD;
            bb.update(pos, i + 1);
        }

        reverse(all(a));
    }
    reverse(s[1], s[1] + n);
    for (size_t i = 0; i < n; i++)
    {
        res = add(res, mul(a[i], add(mul(i + 1, n - i), add(mul(s[0][i], n - i), mul(s[1][i], i + 1)))));
    }
    cout << res << '\n';
}

