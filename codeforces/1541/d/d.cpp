//Here it is an unweighted tree
//If you want to solve for thath then
//You have to go for Dist(a,b) = Dist(a) + Dist(b) - 2*Dist(lca(a,b))
// where Dist(a) is distance from Root to a;
struct LeastCommonAncestor
{
    vector<int> Level;
    vector<vector<int>> dp;
    vector<vector<int>> Adj;
    int Log;
    LeastCommonAncestor() {}
    LeastCommonAncestor(vector<vector<int>> &Tree, int root) : Adj(Tree)
    {
        int n = Tree.size();
        Log = ceil(log2(n)) + 1;
        dp.assign(Log, vector<int>(n));
        Level.assign(n, 0);
        dfs(root, root, 0);
        for (int i = 1; i < Log; ++i)
            for (int j = 0; j < n; ++j)
                dp[i][j] = dp[i - 1][dp[i - 1][j]];
    }
    void dfs(int node, int parent, int level)
    {
        dp[0][node] = parent;
        Level[node] = level;
        for (auto child : Adj[node])
            if (child != parent)
                dfs(child, node, level + 1);
    }
    int lca(int a, int b)
    {
        if (Level[a] > Level[b])
            swap(a, b);
        int d = Level[b] - Level[a];
        for (int i = 0; i < Log; ++i)
            if (d & (1 << i))
                b = dp[i][b];
        if (a == b)
            return a;
        for (int i = Log - 1; i >= 0; --i)
            if (dp[i][a] != dp[i][b])
            {
                a = dp[i][a];
                b = dp[i][b];
            }
        return dp[0][a];
    }
    int dist(int a, int b)
    {
        return Level[a] + Level[b] - 2 * Level[lca(a, b)];
    }
};

void solveCase()
{
    int n;
    cin >> n;
    vector<vector<int>> adj(n);
    for (size_t i = 1; i < n; i++)
    {
        int u, v;
        cin >> u >> v;
        --u, --v;
        adj[u].pb(v);
        adj[v].pb(u);
    }
    int res = 0;
    vector<vector<int>> dp(n + 1, vector<int>(n + 1));
    for (size_t i = 1; i <= n; i++)
        dp[0][i] = 1;
    for (size_t i = 1; i <= n; i++)
        for (size_t j = 1; j <= n; j++)
        {
            dp[i][j] = mul(add(dp[i - 1][j], dp[i][j - 1]), mod_inv(2));
            db(i, j, dp[i][j]);
        }

    for (size_t i = 0; i < n; i++)
    {
        LeastCommonAncestor lca(adj, i);
        for (size_t i = 0; i < n; i++)
        {
            for (size_t j = i + 1; j < n; j++)
            {

                int anc = lca.lca(i, j);
                int left = lca.Level[i] - lca.Level[anc];
                int right = lca.Level[j] - lca.Level[anc];

                db(i, j, dp[right][left]);
                res = add(res, dp[right][left]);
            }
        }
    }
    res = mul(res, mod_inv(n));
    cout << res << '\n';
}

