struct Solution
{
    Solution() {}
    void solveCase();
};

void Solution::solveCase()
{
    int n, k;
    cin >> n >> k;
    int res = INT_MAX;
    if (k == n)
        res = 0;
    else if (k > n)
        res = k - n;
    else if (n % 2 == k % 2)
        res = 0;
    else
        res = 1;
    cout << res << '\n';
}

